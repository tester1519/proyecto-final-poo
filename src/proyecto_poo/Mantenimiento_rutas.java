    private void bt_activarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_activarActionPerformed
        // TODO add your handling code here:
        if("".equals(tb_codigo.getText()))
        {
            JOptionPane.showMessageDialog(this,"Debe seleccionar una ruta para poder activarla");
            return;
        }
        
        Archivo arc = new Archivo();
        try {
            int codigo = Integer.valueOf(tb_codigo.getText());
            Rutas r = (Rutas) arc.buscar_archivo("Rutas",codigo);
            r.setEstado(true);
            arc.actualizar_archivo("Rutas", r,codigo);
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(Mantenimiento_rutas.class.getName()).log(Level.SEVERE, null, ex);
        }
        bt_nuevo.doClick();
    }//GEN-LAST:event_bt_activarActionPerformed


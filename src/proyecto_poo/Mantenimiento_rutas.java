    private void bt_elegirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_elegirActionPerformed
        // TODO add your handling code here:
        Archivo arc = new Archivo();
        int codigo_ruta = Funciones.extraer_codigo(cb_rutas.getSelectedItem().toString());
        try 
        {
            Rutas r = (Rutas) arc.buscar_archivo("Rutas", codigo_ruta);
            this.tb_lugar_salida.setText(r.getLugar_salida());
            this.tb_lugar_llegada.setText(r.getLugar_llegada());
            this.tb_distancia.setText(Float.toString(r.getDistancia()));
            this.tb_codigo.setText(Integer.toString(r.getCodigo_ruta()));
            this.cb_estado.setSelected(r.isEstado());
        } 
        catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(Mantenimiento_rutas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bt_elegirActionPerformed


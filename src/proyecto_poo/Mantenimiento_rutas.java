    private void bt_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_guardarActionPerformed
        // TODO add your handling code here:
        if(!validar())
        {
                JOptionPane.showMessageDialog(this,"Debe llenar todos los campos");
                return;
        }
        
        if("".equals(tb_codigo.getText()))
        {
            Rutas r;
            try {
                tb_codigo.setText(Integer.toString(Funciones.generar_codigo_automatico("Rutas")));
            } catch (IOException | ClassNotFoundException ex) {
                Logger.getLogger(Mantenimiento_rutas.class.getName()).log(Level.SEVERE, null, ex);
            }
            r = recoger_datos();
            Archivo arc = new Archivo();
            arc.insertar_archivo("Rutas", r);
            bt_nuevo.doClick();
        }
        else
        {
            Rutas r = recoger_datos();
            Archivo arc = new Archivo();
            try {
                arc.actualizar_archivo("Rutas",r,r.getCodigo_ruta());
            } catch (IOException | ClassNotFoundException ex) {
                Logger.getLogger(Mantenimiento_rutas.class.getName()).log(Level.SEVERE, null, ex);
            }
            bt_nuevo.doClick();
        }
    }//GEN-LAST:event_bt_guardarActionPerformed


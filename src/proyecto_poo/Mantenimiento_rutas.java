    public boolean validar()
    {
        if("".equals(this.tb_lugar_salida.getText()) || "".equals(this.tb_lugar_llegada.getText()) || "".equals(this.tb_distancia.getText()))
        {
            return false;
        }
        return true;
    }
    
    public void llenar_cb_rutas() throws IOException, FileNotFoundException, ClassNotFoundException
    {
        cb_rutas.removeAllItems();
        Archivo arc = new Archivo();
        ArrayList<Rutas> lista;
        lista = arc.leer_archivo("Rutas");
        String cadena;
        
        for(Rutas elemento : lista)
        {
            cadena = elemento.getCodigo_ruta() + " " + elemento.getLugar_llegada();
            this.cb_rutas.addItem(cadena);
        }
    }
    
    public Rutas recoger_datos()
    {
        
        Rutas r = new Rutas();
        
        r.setCodigo_ruta(Integer.valueOf(tb_codigo.getText()));
        r.setLugar_salida(tb_lugar_salida.getText());
        r.setLugar_llegada(tb_lugar_llegada.getText());
        r.setDistancia(Float.valueOf(tb_distancia.getText()));
        r.setEstado(cb_estado.isSelected());
        
        return r;
    }

